// JavaScript Assignment Operators Examples

// 1. Assignment (=)
// Assigns a value to a variable
let number = 10;
console.log("Assignment (number = 10): " + " " + number); // Output: 10

// 2. Addition Assignment (+=)
// Adds the right operand to the left operand and assigns the result to the left operand
number += 5; // Equivalent to number = number + 5;
console.log("Addition Assignment (number += 5): " + " " + number); // Output: 15

// 3. Subtraction Assignment (-=)
// Subtracts the right operand from the left operand and assigns the result to the left operand
number -= 3; // Equivalent to number = number - 3;
console.log("Subtraction Assignment (number -= 3): " + " " + number); // Output: 12

// 4. Multiplication Assignment (*=)
// Multiplies the left operand by the right operand and assigns the result to the left operand
number *= 2; // Equivalent to number = number * 2;
console.log("Multiplication Assignment (number *= 2): " + " " + number); // Output: 24

// 5. Division Assignment (/=)
// Divides the left operand by the right operand and assigns the result to the left operand
number /= 4; // Equivalent to number = number / 4;
console.log("Division Assignment (number /= 4): " + " " + number); // Output: 6

// 6. Increment (++)
// Increases the value of the operand by 1
number++;
console.log("Increment (number++): " + " " + number); // Output: 7

// 7. Decrement (--)
// Decreases the value of the operand by 1
number--;
console.log("Decrement (number--): " + " " + number); // Output: 6

// You can also chain these operators together:
number += 2; // number is now 8
number *= 3; // number is now 24
number--; // number is now 23
console.log("Chained Operations (number += 2, number *= 3, number--): " + " " + number); // Output: 23
