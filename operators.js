// JavaScript Mathematical Operators Examples

// 1. Addition (+)
// Adds two numbers together
let sum = 5 + 3;
console.log("Addition (5 + 3):", sum); // Output: 8

// 2. Subtraction (-)
// Subtracts one number from another
let difference = 10 - 4;
console.log("Subtraction (10 - 4):", difference); // Output: 6

// 3. Multiplication (*)
// Multiplies two numbers
let product = 7 * 2;
console.log("Multiplication (7 * 2):", product); // Output: 14

// 4. Division (/)
// Divides one number by another
let quotient = 20 / 4;
console.log("Division (20 / 4):", quotient); // Output: 5

// 5. Modulus (%)
// Returns the remainder of a division
let remainder = 10 % 3;
console.log("Modulus (10 % 3):", remainder); // Output: 1

// Breaking it down:

// Addition: The `+` operator takes two numbers (5 and 3) and adds them together.
// Subtraction: The `-` operator subtracts the second number (4) from the first number (10).
// Multiplication: The `*` operator multiplies the two numbers (7 and 2).
// Division: The `/` operator divides the first number (20) by the second number (4).
// Modulus: The `%` operator returns the remainder when the first number (10) is divided by the second number (3).

// You can combine these operators to perform more complex calculations:
let complexCalculation = (5 + 3) * 2 - 4 / 2 % 3;
console.log("Complex Calculation ((5 + 3) * 2 - 4 / 2 % 3):", complexCalculation); 
// Breaking down:
// (5 + 3) = 8
// 8 * 2 = 16
// 4 / 2 = 2
// 2 % 3 = 2 (remainder of 2 divided by 3)
// 16 - 2 = 14
// Final output: 14
